// ─── Helper: get surahs within a Juz with their Juz-start ayah ────────────────

export interface JuzSurahEntry {
  surahNumber: number;
  englishName: string;
  arabicName: string;
  englishNameTranslation: string;
  /** Ayah number where this Juz begins within this surah (1 for all except the first surah of the Juz) */
  startAyah: number;
}

export function getSurahsInJuz(
  juz: JuzInfo,
  allSurahs: { number: number; englishName: string; name: string; englishNameTranslation: string }[]
): JuzSurahEntry[] {
  const result: JuzSurahEntry[] = [];
  for (let s = juz.startSurah; s <= juz.endSurah; s++) {
    const surah = allSurahs.find((x) => x.number === s);
    if (!surah) continue;
    result.push({
      surahNumber: s,
      englishName: surah.englishName,
      arabicName: surah.name,
      englishNameTranslation: surah.englishNameTranslation,
      startAyah: s === juz.startSurah ? juz.startAyah : 1,
    });
  }
  return result;
}

